package org.apache.geronimo.gbean;

import java.io.Serializable;
import java.lang.reflect.Method;
import java.util.ArrayList;
import java.util.Collections;
import java.util.Iterator;
import java.util.List;
import java.util.Set;

import org.apache.geronimo.gbean.GAttributeInfo;

/**
 * Describes a GBean.  This extension allows the properties to be mutable during setup,
 * and once the MBean is deployed an imutable copy of will be made.  This class also adds support for multi target
 * POJOs under the MBean.
 *
 * @version $Revision: 1.1 $ $Date: 2004/01/12 01:38:55 $
 */
public final class GBeanInfo implements Serializable {
    /**
     * Static helper to try to get the GBeanInfo from the class supplied.
     * @param className name of the class to get the GBeanInfo from
     * @param classLoader the class loader use to load the specifiec class
     * @return GBeanInfo generated by supplied class
     * @throws org.apache.geronimo.gbean.InvalidConfigurationException if there is a problem gettting the GBeanInfo info from the class
     */
    public static GBeanInfo getGBeanInfo(String className, ClassLoader classLoader) throws InvalidConfigurationException {
        Class clazz;
        try {
            clazz = classLoader.loadClass(className);
        } catch (ClassNotFoundException e) {
            throw new InvalidConfigurationException("Could not load class " + className, e);
        }
        Method method;
        try {
            method = clazz.getDeclaredMethod("getGBeanInfo", new Class[]{});
        } catch (NoSuchMethodException e) {
            throw new InvalidConfigurationException("Class does not have a getGBeanInfo() method: " + className);
        }
        try {
            return (GBeanInfo) method.invoke(clazz, new Object[]{});
        } catch (Exception e) {
            throw new InvalidConfigurationException("Could not get GBeanInfo from class: " + className, e);
        }
    }

    private final String name;
    private final String description;
    private final String className;
    private final Set attributes;
    private final GConstructorInfo constructor;
    private final Set operations;
    private final Set notifications;
    private final Set endpoints;
    private final List persistentAttributes;

    public GBeanInfo(String className, Set attributes, GConstructorInfo constructor, Set operations, Set endpoints, Set notifications) {
        this(className, null, className, attributes, constructor, operations, endpoints, notifications);
    }

    public GBeanInfo(String name, String description, String className, Set attributes, GConstructorInfo constructor, Set operations, Set endpoints, Set notifications) {
        this.name = name;
        this.description = description;
        this.className = className;
        this.attributes = Collections.unmodifiableSet(attributes);
        if (constructor != null) {
            this.constructor = constructor;
        } else {
            this.constructor = new GConstructorInfo(Collections.EMPTY_LIST, Collections.EMPTY_LIST);
        }
        this.operations = Collections.unmodifiableSet(operations);
        this.endpoints = Collections.unmodifiableSet(endpoints);
        this.notifications = Collections.unmodifiableSet(notifications);

        List attrs = new ArrayList();
        for (Iterator i = attributes.iterator(); i.hasNext();) {
            GAttributeInfo info = (GAttributeInfo) i.next();
            if (info.isPersistent()) {
                attrs.add(info);
            }
        }
        persistentAttributes = Collections.unmodifiableList(attrs);
    }

    public String getName() {
        return name;
    }

    public String getDescription() {
        return description;
    }

    public String getClassName() {
        return className;
    }

    public Set getAttributeSet() {
        return attributes;
    }

    public List getPersistentAttributes() {
        return persistentAttributes;
    }

    public GConstructorInfo getConstructor() {
        return constructor;
    }

    public Set getOperationsSet() {
        return operations;
    }

    public Set getNotificationsSet() {
        return notifications;
    }

    public Set getEndpointsSet() {
        return endpoints;
    }

    public String toString() {
        StringBuffer result = new StringBuffer("[GeronimoMBeanInfo: id=").append(super.toString()).append(" name=").append(name).append(" description=").append(description);
        for (Iterator iterator = attributes.iterator(); iterator.hasNext();) {
            GAttributeInfo geronimoAttributeInfo = (GAttributeInfo) iterator.next();
            result.append("\n    attribute: ").append(geronimoAttributeInfo);
        }
        for (Iterator iterator = operations.iterator(); iterator.hasNext();) {
            GOperationInfo geronimoOperationInfo = (GOperationInfo) iterator.next();
            result.append("\n    operation: ").append(geronimoOperationInfo);
        }
        for (Iterator iterator = endpoints.iterator(); iterator.hasNext();) {
            GEndpointInfo endpointInfo = (GEndpointInfo) iterator.next();
            result.append("\n    endpoint: ").append(endpointInfo);
        }
        result.append("]");
        return result.toString();
    }
}